// logical operations
// logical operators: &&, ||, !
// && is AND operator
// || is OR operator
// ! is NOT operator
let isLoggedIn = true;
let isPaid = false;
console.log("isLoggedIn",isLoggedIn)
console.log("isPaid",isPaid)
console.log("AND Operator", isLoggedIn && isPaid); // false
console.log("OR Operator", isLoggedIn || isPaid); // true
console.log("NOT Operator", !isLoggedIn); // false
console.log("NOT Operator", !isPaid); // true   
